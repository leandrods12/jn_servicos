    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed

        ClienteDAO clienteDAO = new ClienteDAO();
        String nome = txt_nome.getText();
        String endereco = txt_endereco.getText();
        long cpf = Long.parseLong(txt_cpf.getText());
        long telefone = Long.parseLong(txt_telefone.getText());

        Cliente novoCliente = new Cliente(cpf, telefone, nome, endereco);
        if (clienteDAO.inserirNovoCliente(novoCliente)) {
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

